#[derive(Copy, Clone, Eq, PartialEq, Ord, PartialOrd, Debug)]
#[repr(u8)]
pub enum Instruction {
    // Direct stack manipulation
    Const,
    Load,
    Set,
    Pop,
    // Basic operations
    Add,
    Subtract,
    Multiply,
    Divide,
    Remainder,
    IntDivide,
    Power,
    Negate,
    Glue,
    Access,
    Assign,
    Not,
    And,
    Or,
    BitwiseAnd,
    BitwiseOr,
    BitwiseXor,
    BitwiseNeg,
    LeftShift,
    RightShift,
    Cons,
    Leq,
    Lt,
    Geq,
    Gt,
    RefEq,
    ValEq,
    RefNeq,
    ValNeq,
    Compose,
    // Control flow
    Return,
    Invert,
    Jump,
    CondJump,
    Branch,
    Fizzle,
    Exit,
}
